#write a program to demonstrate use of relational operators
x=int(input("enter first number"))
y=int(input("enter second number"))
print(str(x)+"<"+str(y)+"="+str(x<y))
print(str(x)+"=="+str(y)+"="+str(x==y))
print(str(x)+"!="+str(y)+"="+str(x!=y))
print(str(x)+">="+str(y)+"="+str(x>=y))
print(str(x)+"<="+str(y)+"="+str(x<=y))
